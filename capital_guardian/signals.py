"""
capital_guardian/signals.py — Phase 2 Audit History instrumentation.

`pre_save` snapshots a tracked model's prior DB row (if any); `post_save`
diffs the saved instance's tracked fields against that snapshot and writes
one AuditLogEntry per real changed field via services/audit_log.py. A brand
new row logs a single "(created)" entry rather than one per field.

This is connected from CapitalGuardianConfig.ready() to models in OTHER
apps (gold_intelligence.MineTimelineMilestone/EquipmentSpec/CapitalBudgetLine/
GoldProject, evidence_memory.EvidenceMemory) as well as capital_guardian's
own ProjectGovernance/RedFlag — a normal Django cross-app signal pattern that
requires no changes to those apps' own files beyond the additive model
fields already made. `changed_by` is populated only when a call site (an
admin's save_model, or a service) has explicitly set `instance._cg_changed_by`
— every other write (seed commands, the red-flag engine, shell) is honestly
logged with changed_by=None rather than guessing a user.

Idempotent-seed-safe: re-running an update_or_create with identical values
produces zero diffs (old == new for every tracked field), so no audit noise
is generated by a no-op re-seed.
"""
from django.db.models.signals import post_save, pre_save

_PRIOR_ATTR = '_cg_audit_prior_values'


def _snapshot(model_cls, tracked_fields):
    def handler(sender, instance, **kwargs):
        if not instance.pk:
            setattr(instance, _PRIOR_ATTR, None)
            return
        prior = model_cls.objects.filter(pk=instance.pk).values(*tracked_fields).first()
        setattr(instance, _PRIOR_ATTR, prior)
    return handler


def _log(tracked_fields, event_type, project_getter, description_getter, source_reference_getter, field_labels=None):
    def handler(sender, instance, created, **kwargs):
        from capital_guardian.services import audit_log

        project = project_getter(instance)
        if project is None:
            return
        source_reference = source_reference_getter(instance)
        description = description_getter(instance)
        changed_by = getattr(instance, '_cg_changed_by', None)
        prior = getattr(instance, _PRIOR_ATTR, None)

        if prior is None:
            audit_log.record_creation(project, event_type, description, source_reference, changed_by)
            return
        for field in tracked_fields:
            old_value = prior.get(field)
            new_value = getattr(instance, field)
            if old_value != new_value:
                audit_log.record_change(
                    project, event_type, description, (field_labels or {}).get(field, field),
                    old_value, new_value, changed_by=changed_by, source_reference=source_reference,
                )
    return handler


